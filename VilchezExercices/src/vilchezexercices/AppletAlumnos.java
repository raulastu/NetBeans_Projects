    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        i++;
        label1.setText("ingrese datos de alumno " +i+":");
        String nom=textField1.getText();
        int x=Integer.parseInt(textField2.getText());
        
        sumaTotal=sumaTotal + x;
        if(x%2==0){
                pares++;
                totalPares=totalPares + x;
        }							
        else
                impares++;
        if(x>mayor){
                mayor=x;
                alumnoMayor=nom;
        }				
        if(x<menor)
                menor=x; 
        
        textField1.setText("");
        textField2.setText("");
        if(i==10){
            promedio=sumaTotal/(double)10;
            promedioPares=totalPares/(double)pares;
            textArea1.setText("La suma de todas las edades : "+sumaTotal+"\n");
            textArea1.append("La cantidad de edades impares : "+impares+"\n");
            textArea1.append("El promedio de edades : "+promedio+"\n");
            textArea1.append("El promedio de edades pares: "+promedioPares+"\n");
            textArea1.append("La edad mayor : "+ mayor+"\n");
            textArea1.append("La edad menor : "+ menor+"\n");
            textArea1.append("El nombre del Alumno que tiene la mayor edad: "+ alumnoMayor+"\n");
            
            label1.setText("ingrese datos de alumno 1:");
            i=0;
            sumaTotal=0;
            impares=0;
            totalPares=0;
            pares=0;
            promedio=0;
            promedioPares=0;
            mayor=0;
            menor=10000;
            alumnoMayor="";
        }
    }//GEN-LAST:event_button1ActionPerformed
