    public void setTables(){
        ArrayList<Table> data= StoredProcedure.readTables();
        DefaultTableModel dt=(DefaultTableModel) jtName.getModel();
        for(Table da:data)
            dt.addRow(new Object[]{da.getTable()});
    }

     public void setProcedures(){
        refrescarTableProcedure();
        ArrayList<String> data= StoredProcedure.getProcedure();
        DefaultTableModel dti=(DefaultTableModel) jTable2.getModel();
        for(String da:data)
            dti.addRow(new Object[]{da.toString()});
    }

     public void refrescarTableProcedure(){

          DefaultTableModel dt=(DefaultTableModel) jTable2.getModel();
          int cantRow=jTable2.getRowCount();
          for(int i=0;i<cantRow;i++){
              dt.removeRow(0);
          }

     }
    public void mostrar(){
        Operaciones f= new Operaciones(this, true);
        f.setTable(nametable);
        f.setLocationRelativeTo(this);
        f.setVisible(true);
        jTextArea1.setText(f.getScript());
        StoredProcedure.getProcedure();

    }

    public void executeProcedure(){

        String rpta=StoredProcedure.executeProcedure(this.jTextArea1.getText());
       if(rpta==null){
           JOptionPane.showMessageDialog(jTextArea1,"Procedimiento creado con Exito ..!");
         setProcedures();}
       else
           JOptionPane.showMessageDialog(jTextArea1, rpta);

    }
