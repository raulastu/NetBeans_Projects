    private void jtNameMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtNameMouseReleased

        try{
        int row=jtName.getSelectedRow();
        nametable=String.valueOf(jtName.getValueAt(row,0));
         if(evt.isPopupTrigger()){
            jtName.clearSelection();
            popupCreateSP.show(jtName, evt.getX(), evt.getY());
            itemProcedure.setText("Create procedure "+nametable);
        }
        }catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(jTextArea1,"Debe selecionar una Tabla, antes de Hacer Click Derecho ..");
        }catch(IndexOutOfBoundsException ex){

        }
}//GEN-LAST:event_jtNameMouseReleased

