    private void rbCadaUnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbCadaUnoActionPerformed
        System.gc();
        int n = Integer.parseInt(spNroFilosofos.getValue()+"");
        int pensar = Integer.parseInt(spinPensar.getValue()+"");
        int esperar = Integer.parseInt(spinPensar.getValue()+"");
        int comer = Integer.parseInt(spComer.getValue()+"");
        Estado estado = Estado.valueOf(cmbEstado.getSelectedItem()+"");
        JPanel pan = new JPanel(new GridLayout(n, 1));
        filos = new FilosofoValuesPanel[n];
        for (int i = 0; i<filos.length; i++) {
            filos[i] = new FilosofoValuesPanel(pensar, esperar, comer, estado);
            pan.add(filos[i]);
        }
        scpFP.setViewportView(pan);
        filosofosSelected = true;
        checkButtonState();
    }//GEN-LAST:event_rbCadaUnoActionPerformed

    private void butCenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butCenarActionPerformed

        if (rbTodos.isSelected()) {
            cena = new Cena();
            int n = Integer.parseInt(spNroFilosofos.getValue()+"");
            Estado estado = Estado.valueOf(cmbEstado.getSelectedItem()+"");
            int pensar = Integer.parseInt(spinPensar.getValue()+"");
            int esperar = Integer.parseInt(spinPensar.getValue()+"");
            int comer = Integer.parseInt(spComer.getValue()+"");
            for (int i = 0; i<n; i++) {
                cena.ingresar(estado, pensar, esperar, comer);
            }
            RCContainer.showPanel(new CenaGui(cena));

            cena.iniciarHilos();
        } else if (rbCadaUno.isSelected()) {
            cena = new Cena();
            for (int i = 0; i<filos.length; i++) {
                Estado estado = Estado.valueOf(filos[i].cmbEstado.getSelectedItem()+"");
                int pensar = Integer.parseInt(filos[i].spPensar.getValue()+"");
                int esperar = Integer.parseInt(filos[i].spEsperar.getValue()+"");
                int comer = Integer.parseInt(filos[i].spComer.getValue()+"");
                cena.ingresar(estado, pensar, esperar, comer);
            }
            RCContainer.showPanel(new CenaGui(cena));
            cena.iniciarHilos();
        }

}//GEN-LAST:event_butCenarActionPerformed

