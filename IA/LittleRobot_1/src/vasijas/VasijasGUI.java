    private void butBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butBuscarActionPerformed
//        table.setModel(new DefaultTableModel(new String[][]{{"Buscando Solución"}},
//                new String[]{"Procesando"}));
//        table.revalidate();
        vasijas.x_max = Integer.parseInt(spinXMAX.getValue() + "");
        vasijas.y_max = Integer.parseInt(spinYMAX.getValue() + "");
        vasijas.setEstadoInicial(new Estado(Integer.parseInt(spinXinit.getValue() + ""),
                Integer.parseInt(spinYinit.getValue() + "")));
        vasijas.setEstadoFinal(new Estado(Integer.parseInt(spinXTarget.getValue() + ""),
                Integer.parseInt(spinYTarget.getValue() + "")));
        vasijas.savingData=true;
        vasijas.resolver();
        if (vasijas.solved) {
            table.setModel(new TableModel());
            for (int i = 0; i < columnNames.length; i++) {
                table.getColumnModel().getColumn(i).setPreferredWidth(Integer.parseInt(columnNames[i][1]));
            }
        } else {
            table.setModel(new DefaultTableModel(new String[][]{{"No existe solución"}},
                    new String[]{"what a pity"}));
        }
    }//GEN-LAST:event_butBuscarActionPerformed

